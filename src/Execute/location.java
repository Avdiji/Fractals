package Execute;

public class location {

	private int x;
	private int y;

	public location(final int x, final int y) {
		this.x = x;
		this.y = y;

	}

	/**
	 * 
	 * @return x starting location
	 */
	public int getx() {
		return this.x;
	}

	/**
	 * 
	 * @return y starting location
	 */
	public int gety() {
		return this.y;
	}

}
